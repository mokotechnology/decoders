import { describe, test, expect } from "vitest";

import { decoderRun } from "../../../../../src/functions/decoder-run";

const file_path =
  "decoders/connector/decentlab/dl-ctd10/v1.0.0/payload.js" as const;

  describe("Payload decoder test 1", () => {
  const payload = [
    {
      variable: "payload",
      value: "0207d9000390888081006400000c60",
    },
  ];
  const decoded = decoderRun(file_path, { payload });
  const values = Object.fromEntries(
    decoded.map((it) => [it.variable, it.value ?? 0]),
  );

  test("Decoded values", () => {
    expect(values.battery_voltage).toBe(3.17);
    expect(values.device_id).toBe(2009);
    expect(values.electrical_conductivity).toBe(100);
    expect(values.freezing_flag).toBe(0);
    expect(values.protocol_version).toBe(2);
    expect(values.temperature).toBe(12.9);
    expect(values.water_depth).toBe(4232);
  });
});

